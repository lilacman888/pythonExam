# 비교연산자 : ==, !=, >, >=, <, <=
# 비교연산자의 결과가 참이면 True, 거짓이면 False를 리턴

x = 10
y = 20
str1 = 'abc'
str2 = 'python'

print('x == y : ', x == y )
print('x != y : ', x != y )
print('x > y : ', x > y )
print('x >= y : ', x >= y )
print('x < y : ', x < y )
print('x <= y : ', x <= y )

print('str1 == str2 : ',str1 == str2)
print('str2 == python : ', str2 == 'python')
print('str1 < str2 : ', str1 < str2)
# 문자열의 크기 비교는 사전순서로 비교한다.