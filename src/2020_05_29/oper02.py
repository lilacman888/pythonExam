# 산술연산자
# +, -, *, /(몫:정수형), %(나머지), **(거듭제곱)

a = 10; b = 3;
print(a)
print(b)

result1 = a + b
result2 = a - b
result3 = a * b
result4 = a / b
result5 = a // b
result6 = a % b
result7 = a ** b

print('result1=', result1)      # 더하기
print('result2=', result2)      # 빼기
print('result3=', result3)      # 곱하기
print('result4=', result4)      # 나누기
print('result5=', result5)      # 나누기(몫)
print('result6=', result6)      # 나누기(나머지)
print('result7=', result7)      # 거듭제곱
